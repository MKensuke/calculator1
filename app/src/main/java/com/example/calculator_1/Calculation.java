package com.example.calculator_1;

public class Calculation {
    int calculateResult;

    private int add(int inputNum1, int inputNum2){
        calculateResult = inputNum1 + inputNum2;
        return calculateResult;
    }
    private int subtract(int inputNum1, int inputNum2){
        calculateResult = inputNum1 - inputNum2;
        return calculateResult;
    }
    private int multiply(int inputNum1, int inputNum2){
        calculateResult = inputNum1 + inputNum2;
        return calculateResult;
    }
    private int divide(int inputNum1, int inputNum2){
        calculateResult = inputNum1 + inputNum2;
        return calculateResult;
    }
}
